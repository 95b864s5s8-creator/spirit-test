  // ==================== 对外 API ====================

  /**
   * 获取叠加态名称
   * @param {string} camp - 'NT' | 'NF' | 'SJ' | 'SP'
   * @param {string} cureElement - '木' | '火' | '土' | '金' | '水'
   * @returns {string} 命名（如"空心智囊"）
   */
  function getName(camp, cureElement) {
    var key = camp + '_' + cureElement;
    return NAMES[key] || '未知形态';
  }

  /**
   * 获取完整文案（含总述+四段拆解）
   * @param {string} camp - 'NT' | 'NF' | 'SJ' | 'SP'
   * @param {string} cureElement - '木' | '火' | '土' | '金' | '水'
   * @returns {object|null} { name, summary, mask, deficit, collision, s_result, w_result }
   */
  function getProfile(camp, cureElement) {
    var key = camp + '_' + cureElement;
    return PROFILES[key] || null;
  }

  /**
   * 获取带S/W后缀的完整名称
   * @param {string} camp
   * @param {string} cureElement
   * @param {boolean} isStrong - true=S(外化), false=W(内化)
   * @returns {string} 如"空心智囊-S"
   */
  function getFullName(camp, cureElement, isStrong) {
    var name = getName(camp, cureElement);
    return name + (isStrong ? '-S' : '-W');
  }

  /**
   * 获取S/W一句话标签
   * @param {boolean} isStrong
   * @returns {string}
   */
  function getSWLabel(isStrong) {
    return isStrong ? SW_LABELS.S : SW_LABELS.W;
  }

  // 暴露到全局
  return {
    NAMES: NAMES,
    SW_LABELS: SW_LABELS,
    PROFILES: PROFILES,
    getName: getName,
    getProfile: getProfile,
    getFullName: getFullName,
    getSWLabel: getSWLabel,
  };

